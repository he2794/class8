package class3;

public class ShortHandOperator4 {
    public static void main(String[] args) {

        int number=10;
      //  number=number+10;
        number*=20;//number+: previous value//200----10*20
        System.out.println(number);

        int number2=20;
        number2-=10;// 10-20
        System.out.println(number2);

        int number3=100;
        number3/=10; //100/10=10;
        System.out.println(number3);//10


    }
}
