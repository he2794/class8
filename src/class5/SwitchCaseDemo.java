package class5;

public class SwitchCaseDemo {
    public static void main(String[] args) {

        int day=2;
        if (day==1){
            System.out.println(" Monday");

        } else if (day==2) {
            System.out.println("Tuesday");
        } else if (day==3) {
            System.out.println("Wednesday");
        }else{
            System.out.println("Wrong day");
        }


    }
}
